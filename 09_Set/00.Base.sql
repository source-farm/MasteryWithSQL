-- В SQL есть несколько операторов над множествами. Они позволяют
-- объединить результат выполнения запросов SELECT в одну таблицу.
-- Есть три таких оператора:
--
--    UNION [ALL]
--    INTERSECT [ALL]
--    EXCEPT [ALL]
--
-- По-умолчанию эти операторы убирают дубликаты строк из результата, но
-- если после оператора добавить ключевое слово ALL, то дубликаты остаются.
-- То что делает UNION и INTERSECT интуитивно понятно, а вот EXCEPT - это
-- вычитывание результата одного SELECT из другого. Здесь в отличие от
-- UNION и INTERSECT порядок исходных таблиц уже имеет значение.
